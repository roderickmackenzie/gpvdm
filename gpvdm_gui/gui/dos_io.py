kb=1.3806504e-23
Q=1.602176487e-19
from numpy import log
from numpy import exp

def gen_fermi_from_np(n,N,T):
	return -kb*T*log(n/N)/Q

def gen_np_from_fermi(Fn,N,T):
	return N*exp(-Q*Fn/(kb*T))

